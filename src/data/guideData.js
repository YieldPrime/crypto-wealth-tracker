export const getRandomQuote = () => {
  const quotes = guideData.motivationalQuotes;
  return quotes[Math.floor(Math.random() * quotes.length)];
};

export const getRankByXP = (xp) => {
  const ranks = [...guideData.ranks].reverse();
  return ranks.find(rank => xp >= rank.minXP) || guideData.ranks[0];
};

export const getNextRank = (xp) => {
  const ranks = guideData.ranks;
  const currentIndex = ranks.findIndex(rank => xp < rank.minXP);
  return currentIndex === -1 ? null : ranks[currentIndex];
};

export const calculateProgress = (progress) => {
  const totalSections = guideData.chapters.reduce(
    (acc, chapter) => acc + chapter.sections.length * 2, 
    0
  );
  
  let completed = 0;
  Object.values(progress).forEach(chapter => {
    Object.values(chapter).forEach(section => {
      if (section.understood) completed++;
      if (section.implemented) completed++;
    });
  });
  
  return Math.round((completed / totalSections) * 100);
};
